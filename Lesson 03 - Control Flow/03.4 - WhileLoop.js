//Common while Loop
let i = 0;
while (i <= 5) {
  console.log(i);
  i++;
}

//Do while loop
//diffrense is that it will first print j then check the condition while in common it checks the condition then runs the code block inside it.
let j = 0;
do {
  console.log(j);
  j++;
} while (j <= 5);

