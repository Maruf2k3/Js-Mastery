let day;

switch (day) {
  case "sunday":
    console.log("Its Sunday!");
    break;
  case "monday":
    console.log("Its Monday");
    break;
  case "tuesday":
    console.log("Its Tuesday");
    break;
  case "wednesday":
    console.log("Its Wednesday");
    break;
  case "thursday":
    console.log("Its Thursday");
    break;
  case "friday":
    console.log("Its Friday");
    break;
  case "saturday":
    console.log("Its Saturday");
    break;
  default:
    console.log("Its Not A Day!");
}
